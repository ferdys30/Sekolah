                                <h4>Bentuk Normal Kedua (Second Normal Form - 2NF)</h4>
                                <h5>a. Definisi Second Normal Form (2NF)</h5>
                                <p>Bentuk normal kedua adalah tabel yang sudah berada di 1NF, dan semua kolom non-kunci sepenuhnya bergantung pada seluruh kunci utama. Ini berarti tidak ada ketergantungan sebagian antara kolom non-kunci dengan bagian dari kunci utama.</p>
                                
                                <h5>b. Syarat Second Normal Form (2NF)</h5>
                                <ul>
                                    <li>Sudah berada di 1NF.</li>
                                    <li>Setiap kolom non-kunci sepenuhnya bergantung pada seluruh kunci utama, bukan sebagian kunci utama.</li>
                                </ul>
                                
                                <h5>c. Contoh Second Normal Form (2NF)</h5>
                                <p>Misalkan kita memiliki tabel berikut di 1NF:</p>
                                <table class="table">
                                    <thead>
