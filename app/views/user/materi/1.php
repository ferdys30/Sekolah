                                <h3>Bentuk-Bentuk Normalisasi</h3>
                                
                                <h4>Definisi Normalisasi</h4>
                                <p>Normalisasi adalah proses pengorganisasian tabel dalam basis data untuk meminimalkan duplikasi data dan menghindari anomali dalam operasi penyisipan, penghapusan, dan pembaruan. Normalisasi membagi data menjadi tabel yang lebih kecil dan saling terkait melalui kunci.</p>
                                
                                <h4>Tingkat atau Bentuk Normalisasi</h4>
                                <p>Normalisasi memiliki beberapa tahap atau bentuk, yang dikenal sebagai First Normal Form (1NF), Second Normal Form (2NF), Third Normal Form (3NF), dan bentuk normal yang lebih lanjut seperti Boyce-Codd Normal Form (BCNF). Setiap bentuk normal bertujuan untuk mengatasi masalah-masalah tertentu dalam struktur basis data.</p>

                                <h4>Proses-Proses Normalisasi Data</h4>
                                <h5>1. Bentuk Tidak Normal (Unnormalized Form - UNF):</h5>
                                <p>Merupakan kondisi awal di mana data belum diorganisir ke dalam tabel. Biasanya, data di bentuk tidak normal masih berbentuk daftar atau laporan, yang mengandung pengulangan atau grup data yang kompleks.</p>
                                
                                <h5>2. Proses Menuju First Normal Form (1NF):</h5>
                                <p>Untuk mencapai bentuk normal pertama, data harus dipecah menjadi tabel, dan setiap tabel harus memiliki kolom yang hanya menyimpan nilai atomik (tidak ada grup berulang). Setiap kolom dalam tabel harus mengandung satu nilai saja, tidak boleh ada multivalued attributes atau array.</p>
                                
                                <h5>3. Proses Menuju Second Normal Form (2NF):</h5>
                                <p>Setelah tabel berada di 1NF, tabel harus diubah menjadi 2NF. Syaratnya, tabel harus bebas dari ketergantungan sebagian. Ini berarti setiap kolom non-kunci harus bergantung sepenuhnya pada seluruh kunci utama, bukan hanya sebagian dari kunci utama.</p>
                                
                                <h5>4. Proses Menuju Third Normal Form (3NF):</h5>
                                <p>Setelah tabel mencapai 2NF, untuk mencapai 3NF, semua ketergantungan transitif harus dihapus. Artinya, tidak boleh ada kolom non-kunci yang bergantung pada kolom non-kunci lainnya. Setiap kolom non-kunci harus bergantung langsung hanya pada kunci utama.</p>

                                <h4>Bentuk Tidak Normal (Unnormalized Form - UNF)</h4>
                                <h5>a. Definisi Bentuk Tidak Normal</h5>
                                <p>Bentuk tidak normal (UNF) adalah bentuk awal di mana data tidak terstruktur secara formal ke dalam tabel. Pada bentuk ini, data biasanya diorganisir dalam bentuk yang mengandung pengulangan atau grup data kompleks. Bentuk ini masih mengandung grup berulang yang perlu dipecah.</p>
                                
                                <h5>b. Contoh Bentuk Tidak Normal</h5>
                                <p>Misalnya, sebuah tabel menyimpan data penjualan dengan produk yang dibeli oleh pelanggan dalam satu kolom:</p>
                                <table class="table">
                                    <thead>
                                        <tr>
                                            <th>ID Penjualan</th>
                                            <th>Nama Pelanggan</th>
                                            <th>Produk</th>
                                        </tr>
                                    </thead>
                                    <tbody>
                                        <tr>
                                            <td>1</td>
                                            <td>Andi</td>
                                            <td>Sepatu, Kaos, Jaket</td>
                                        </tr>
                                        <tr>
                                            <td>2</td>
                                            <td>Budi</td>
                                            <td>Tas, Dompet</td>
                                        </tr>
                                    </tbody>
                                </table>
                                <p>Dalam contoh ini, kolom Produk masih mengandung nilai yang berulang dalam satu entri, sehingga data ini belum berada dalam bentuk normal pertama (1NF).</p>

                                <hr>

                                <h4>Bentuk Normal Pertama (First Normal Form - 1NF)</h4>
                                <h5>a. Definisi First Normal Form (1NF)</h5>
                                <p>Bentuk normal pertama adalah tahap di mana setiap kolom dalam tabel hanya boleh menyimpan nilai atomik, artinya tidak ada nilai yang berulang atau bergrup di dalam kolom. Setiap tabel di 1NF harus memiliki kunci utama yang unik untuk mengidentifikasi setiap baris.</p>
                                
                                <h5>b. Syarat First Normal Form (1NF)</h5>
                                <ul>
                                    <li>Setiap kolom berisi nilai tunggal (tidak ada grup atau array).</li>
                                    <li>Tidak ada baris yang identik dalam tabel (harus ada kunci utama).</li>
                                </ul>
                                
                                <h5>c. Contoh First Normal Form (1NF)</h5>
                                <p>Data yang tidak normal tadi dapat diubah menjadi 1NF dengan cara memisahkan produk ke dalam baris yang berbeda:</p>
                                <table class="table">
                                    <thead>
                                        <tr>
                                            <th>ID Penjualan</th>
                                            <th>Nama Pelanggan</th>
                                            <th>Produk</th>
