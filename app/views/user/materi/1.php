                                <p class="fs-3 fw-bold mt-4">Definisi Basis Data</p>
                                <p>Basis data (database) adalah kumpulan data yang disimpan secara sistematis dan dapat diakses, dikelola, serta diperbarui dengan mudah. Data dalam basis data biasanya diorganisasikan dalam bentuk tabel yang berisi baris dan kolom. Basis data memungkinkan penyimpanan informasi dalam jumlah besar dengan cara yang efisien.</p>

                                <p class="fs-3 fw-bold mt-4">Komponen Basis Data</p>
                                <ul>
                                    <li><strong>Data:</strong> Informasi atau fakta yang disimpan dalam basis data.</li>
                                    <li><strong>Perangkat Keras (Hardware):</strong> Komputer dan perangkat penyimpanan tempat basis data dioperasikan.</li>
                                    <li><strong>Perangkat Lunak (Software):</strong> Sistem manajemen basis data (DBMS) dan perangkat lunak lain yang mendukung pengelolaan data.</li>
                                    <li><strong>Pengguna (Users):</strong> Orang yang mengakses dan mengelola data, seperti administrator dan pengguna akhir.</li>
                                    <li><strong>Prosedur:</strong> Langkah-langkah dan aturan yang digunakan untuk mengelola dan memelihara basis data.</li>
                                </ul>

                                <p class="fs-3 fw-bold mt-4">Sistem Manajemen Basis Data (DBMS)</p>
                                <p>DBMS (Database Management System) adalah perangkat lunak yang digunakan untuk membuat, mengelola, dan memanipulasi basis data. DBMS bertanggung jawab dalam pengelolaan data seperti memasukkan, memperbarui, menghapus, dan menampilkan data. Contoh DBMS yang populer adalah MySQL, PostgreSQL, dan Oracle.</p>

                                <p class="fs-3 fw-bold mt-4">Tujuan dan Manfaat Basis Data</p>
                                <p><strong>Tujuan Basis Data:</strong></p>
                                <ol>
                                    <li>Mempermudah pengelolaan data dalam jumlah besar.</li>
                                    <li>Menjamin keakuratan dan konsistensi data.</li>
                                    <li>Memudahkan pencarian dan pengambilan data.</li>
                                    <li>Menghindari duplikasi data.</li>
                                </ol>
                                <p><strong>Manfaat Basis Data:</strong></p>
                                <ol>
                                    <li>Efisiensi penyimpanan: Data diorganisasikan dengan baik sehingga menghemat ruang penyimpanan.</li>
                                    <li>Keamanan data: Data terlindungi dari akses yang tidak sah.</li>
                                    <li>Kemudahan akses: Pengguna dapat dengan mudah mengakses data kapan saja dibutuhkan.</li>
                                    <li>Konsistensi data: DBMS menjaga agar data yang dimasukkan ke dalam sistem konsisten dan benar.</li>
                                </ol>

                                <p class="fs-3 fw-bold mt-4">Pengguna dalam Basis Data</p>
                                <ul>
                                    <li><strong>Administrator Basis Data (Database Administrator/DBA):</strong> Orang yang bertanggung jawab untuk mengelola seluruh sistem basis data, termasuk instalasi, konfigurasi, dan keamanan basis data.</li>
                                    <li><strong>Pengembang Basis Data (Database Developer):</strong> Orang yang membuat dan mengelola struktur basis data, termasuk membuat tabel, hubungan antar tabel, dan membuat query untuk menampilkan data.</li>
                                    <li><strong>Pengguna Akhir (End User):</strong> Orang yang menggunakan data yang disimpan dalam basis data untuk keperluan operasional sehari-hari.</li>
                                </ul>

                                <p class="fs-3 fw-bold mt-4">Operasi-operasi Dasar Manajemen Basis Data</p>
                                <ul>
                                    <li>Membuat basis data: Proses membuat kerangka basis data baru.</li>
                                    <li>Memasukkan data (Insert): Menambahkan data baru ke dalam tabel.</li>
                                    <li>Mengubah data (Update): Memperbarui data yang sudah ada dalam tabel.</li>
                                    <li>Menghapus data (Delete): Menghilangkan data dari tabel.</li>
                                    <li>Mengambil data (Select): Menampilkan data dari tabel berdasarkan kebutuhan pengguna.</li>
                                </ul>

                                <p class="fs-3 fw-bold mt-4">Pengenalan File, Tabel, Record, dan Field</p>
                                <ul>
                                    <li><strong>File:</strong> Kumpulan data yang disimpan di dalam komputer, biasanya berisi informasi terkait satu subjek tertentu.</li>
                                    <li><strong>Tabel:</strong> Kumpulan data yang terorganisir dalam baris dan kolom. Setiap tabel memiliki nama unik.</li>
                                    <li><strong>Record (Baris):</strong> Satu baris data dalam tabel yang merepresentasikan satu unit data. Contoh: satu record berisi data satu siswa.</li>
                                    <li><strong>Field (Kolom):</strong> Setiap kolom dalam tabel yang menyimpan satu jenis informasi. Contoh: kolom nama, alamat, dan nomor telepon.</li>
                                </ul>
