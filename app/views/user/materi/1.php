                                            <td>3</td>
                                        </tr>
                                        <tr>
                                            <td>2</td>
                                            <td>102</td>
                                            <td>5</td>
                                        </tr>
                                    </tbody>
                                </table>
                                <p>Jika <strong>ID Penjualan</strong> dan <strong>Produk ID</strong> adalah kunci utama gabungan, maka <strong>Jumlah</strong> sepenuhnya bergantung pada kombinasi ID Penjualan dan Produk ID.</p>

