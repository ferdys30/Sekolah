                                <p class="fs-3 fw-bold mt-4">Relasi dalam ERD</p>
                                <p>Relasi adalah hubungan antara dua atau lebih entitas. Relasi ini menunjukkan bagaimana entitas berinteraksi satu sama lain. Contohnya, dalam sebuah sistem akademik, ada hubungan antara entitas "Siswa" dan "Kelas." Siswa mengambil kelas, sehingga ada relasi "Mengambil" antara entitas "Siswa" dan "Kelas."</p>
                                <p><strong>Ada beberapa tipe relasi dalam ERD:</strong></p>
                                <ul>
                                    <li><strong>One-to-One (1:1):</strong> Setiap entitas di dalam satu tabel hanya berhubungan dengan satu entitas di tabel lain. Contoh: "Siswa" dan "Kartu Pelajar."</li>
                                    <li><strong>One-to-Many (1:M):</strong> Satu entitas di dalam satu tabel dapat berhubungan dengan banyak entitas di tabel lain. Contoh: "Guru" mengajar banyak "Kelas."</li>
                                    <li><strong>Many-to-Many (M:M):</strong> Banyak entitas di satu tabel dapat berhubungan dengan banyak entitas di tabel lain. Contoh: "Siswa" mengambil banyak "Mata Pelajaran" dan setiap "Mata Pelajaran" bisa diambil oleh banyak siswa.</li>
                                </ul>
                                <p>Setiap relasi dapat memiliki atribut relasional yang menjelaskan detail dari hubungan antar entitas tersebut.</p>

                                <p class="fs-3 fw-bold mt-4">Menemukan Relasi</p>
                                <p>Untuk menemukan relasi, langkah-langkah berikut dapat diikuti:</p>
                                <ol>
                                    <li><strong>Identifikasi Entitas yang Berhubungan:</strong> Tentukan entitas mana saja yang mungkin memiliki interaksi atau keterkaitan dalam sistem. Misalnya, dalam sistem sekolah, entitas "Siswa" dan "Kelas" jelas memiliki hubungan.</li>
                                    <li><strong>Tentukan Jenis Hubungan:</strong> Setelah menemukan entitas yang berhubungan, tentukan jenis hubungan mereka, apakah one-to-one, one-to-many, atau many-to-many. Contoh: hubungan antara "Siswa" dan "Kelas" adalah one-to-many, karena satu kelas diikuti oleh banyak siswa.</li>
                                    <li><strong>Tambahkan Atribut Relasional Jika Diperlukan:</strong> Beberapa relasi mungkin memerlukan atribut tambahan untuk menjelaskan detail hubungan. Misalnya, dalam relasi "Mengambil" antara "Siswa" dan "Kelas," atribut relasional seperti "Nilai" bisa ditambahkan untuk menyimpan informasi nilai siswa.</li>
                                    <li><strong>Pertimbangkan Batasan Partisipasi:</strong> Tentukan apakah relasi membutuhkan partisipasi total atau parsial dari entitas. Misalnya, setiap "Siswa" harus mengambil setidaknya satu "Kelas" (partisipasi total), tetapi tidak semua "Guru" mengajar (partisipasi parsial).</li>
                                </ol>
