                                <p class="fs-3 fw-bold mt-4">Definisi ERD (Entity-Relationship Diagram)</p>
                                <p>Entity-Relationship Diagram (ERD) adalah diagram yang digunakan untuk memodelkan hubungan antara entitas dalam basis data. ERD membantu perancang basis data memvisualisasikan bagaimana entitas saling berhubungan dan bagaimana data dikelompokkan secara logis.</p>
                                <p><strong>Komponen utama dalam ERD:</strong></p>
                                <ul>
                                    <li><strong>Entitas:</strong> Objek atau konsep yang informasinya disimpan di dalam basis data, seperti "Siswa" atau "Mata Pelajaran."</li>
                                    <li><strong>Relasi:</strong> Hubungan antara dua atau lebih entitas. Misalnya, seorang siswa dapat terhubung dengan mata pelajaran yang mereka ambil.</li>
                                    <li><strong>Atribut:</strong> Properti atau karakteristik dari sebuah entitas. Contoh: Nama, Alamat, atau NIS dari entitas "Siswa."</li>
                                    <li><strong>Key:</strong> Atribut yang digunakan untuk mengidentifikasi secara unik entitas tertentu. Misalnya, NIS adalah key untuk entitas "Siswa."</li>
                                </ul>
                                <p>ERD sering digunakan dalam tahap desain basis data untuk memastikan struktur basis data yang logis dan konsisten.</p>

                                <p class="fs-3 fw-bold mt-4">Menemukan Entitas</p>
                                <p>Dalam pemodelan basis data, entitas adalah objek atau konsep yang representasinya perlu disimpan dalam basis data. Untuk menemukan entitas, kita harus mengidentifikasi objek atau hal nyata yang menjadi fokus penyimpanan data.</p>
                                <p><strong>Langkah-langkah untuk menemukan entitas:</strong></p>
                                <ol>
                                    <li>Identifikasi subjek utama: Tentukan subjek atau objek utama yang akan dikelola. Contoh: dalam sistem sekolah, entitas utama mungkin "Siswa," "Guru," "Kelas."</li>
                                    <li>Fokus pada hal yang unik: Pastikan setiap entitas yang ditemukan memiliki makna unik dan dapat diidentifikasi secara terpisah dari entitas lainnya.</li>
                                    <li>Jangan terlalu spesifik: Entitas harus cukup umum untuk menghindari terlalu banyak entitas yang spesifik. Misalnya, "Buku" mungkin merupakan entitas yang lebih baik daripada "Buku Fisika" atau "Buku Matematika."</li>
                                </ol>
                                <p><strong>Contoh:</strong></p>
                                <ul>
                                    <li>Dalam sistem perpustakaan, entitas dapat berupa "Anggota Perpustakaan," "Buku," dan "Transaksi Peminjaman."</li>
                                </ul>

                                <p class="fs-3 fw-bold mt-4">Menemukan Atribut</p>
                                <p>Setelah entitas ditemukan, langkah selanjutnya adalah menentukan atribut, yang merupakan properti atau karakteristik yang perlu disimpan untuk setiap entitas.</p>
                                <p><strong>Langkah-langkah untuk menemukan atribut:</strong></p>
                                <ol>
                                    <li>Identifikasi informasi yang relevan: Pertimbangkan informasi yang harus disimpan untuk setiap entitas. Misalnya, untuk entitas "Siswa," kita mungkin ingin menyimpan nama, NIS, dan alamat.</li>
                                    <li>Pastikan relevansi data: Hanya simpan atribut yang relevan dan diperlukan untuk tujuan sistem. Misalnya, menyimpan "warna kesukaan siswa" mungkin tidak relevan untuk sistem akademik.</li>
                                    <li>Kelompokkan atribut serupa: Jika ada atribut yang mirip atau saling berkaitan, mereka dapat dikelompokkan untuk memudahkan manajemen data. Misalnya, atribut alamat dapat dipecah menjadi "Jalan," "Kota," dan "Kode Pos."</li>
                                </ol>
                                <p><strong>Contoh:</strong></p>
                                <ul>
                                    <li>Untuk entitas "Siswa," atribut yang relevan bisa mencakup:</li>
                                    <ul>
                                        <li>Nama</li>
                                        <li>NIS (Nomor Induk Siswa)</li>
                                        <li>Alamat</li>
                                        <li>Tanggal Lahir</li>
                                    </ul>
                                </ul>
