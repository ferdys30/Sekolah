                                <h4>Ketergantungan Transitif</h4>
                                <h5>a. Definisi</h5>
                                <p>Ketergantungan transitif terjadi ketika atribut A menentukan B, dan B menentukan C, sehingga secara transitif A menentukan C. Atribut C tidak bergantung langsung pada A, tetapi melalui B.</p>
                                <p>Contoh: Pada tabel Siswa:</p>
                                <table class="table">
