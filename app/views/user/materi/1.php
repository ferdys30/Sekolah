                                <h4>Ketergantungan Fungsional</h4>
                                <h5>a. Definisi Ketergantungan Fungsional</h5>
                                <p>Ketergantungan fungsional terjadi ketika atribut A dalam tabel secara unik menentukan atribut B. Artinya, jika dua record memiliki nilai yang sama untuk atribut A, maka nilai B mereka juga harus sama.</p>
                                <p>Contoh: Pada tabel Siswa:</p>
                                <table class="table">
                                    <thead>
                                        <tr>
                                            <th>NIS</th>
                                            <th>Nama</th>
                                            <th>Kelas</th>
                                            <th>Alamat</th>
                                        </tr>
                                    </thead>
                                    <tbody>
                                        <tr>
                                            <td>101</td>
                                            <td>Andi</td>
                                            <td>10A</td>
                                            <td>Jakarta</td>
                                        </tr>
                                        <tr>
                                            <td>102</td>
                                            <td>Budi</td>
                                            <td>11B</td>
                                            <td>Bandung</td>
                                        </tr>
                                    </tbody>
                                </table>
                                <p>Di sini, <strong>NIS → Nama</strong> berarti bahwa setiap NIS unik menentukan nama siswa.</p>
                                
                                <hr>
                                
                                <h4>Ketergantungan Fungsional Penuh</h4>
                                <h5>a. Definisi</h5>
                                <p>Ketergantungan fungsional penuh terjadi ketika suatu atribut bergantung sepenuhnya pada kombinasi dari semua atribut kunci utama (primary key). Jika ada ketergantungan pada sebagian dari atribut kunci utama, maka ketergantungan tersebut tidak dianggap penuh.</p>
                                <p>Contoh: Dalam tabel Penjualan dengan atribut ID Penjualan, Produk ID, dan Jumlah:</p>
                                <table class="table">
                                    <thead>
                                        <tr>
                                            <th>ID Penjualan</th>
                                            <th>Produk ID</th>
                                            <th>Jumlah</th>
                                        </tr>
